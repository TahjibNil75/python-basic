import threading
import time


def boil_milk():
    print("Boiling milk...")
    time.sleep(2)
    print("Milk boiled.")


def toast_bread():
    print("Toasting bread...")
    time.sleep(3)
    print("Bread toasted.")


start = time.time()
t1 = threading.Thread(target=boil_milk)
t2 = threading.Thread(target=toast_bread)

t1.start()
t2.start()

t1.join()
t2.join()
end = time.time()
print(f"Breakfast ready in {end - start} seconds.")